package com.prasit;

public class Loops {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println("Hello, World!");
        }
        /*
        Syntax:
        for (initialisation; condition; increment/decrement) {
            //body
        }
         */

        // While Loop
        /*
        Syntax:
        initialisation;
        while ( condition) {
            //body
            increment/decrement
        }
         */

        //Do While Loop
        /*
         do {
            //body
            increment/decrement
        } while(condition);
         */
    }
}
